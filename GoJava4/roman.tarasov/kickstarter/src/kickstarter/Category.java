package kickstarter;

public class Category {
	String name;
	String description;
	int id;

	void setDescription(String Description) {
		this.description = Description;
	}

	String getDescription() {
		return description;
	}

	void setName(String name) {
		this.name = name;
	}

	String getName() {
		return name;
	}

	void setId(int id) {
		this.id = id;
	}

	int getId() {
		return id;
	}

}
